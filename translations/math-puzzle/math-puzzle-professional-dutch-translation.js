/**
 * Haal Nederlandse vertaling op voor een sleutel
 * @param {string} key - Vertalingssleutel
 * @param {object} params - Parameters voor interpolatie
 * @returns {string} Vertaalde tekst
 */
function getDutchMathPuzzleTranslation(key, params = {}) {
  const translation = MATH_PUZZLE_TRANSLATIONS_NL.nl[key] || key;
  return formatDutchMathPuzzleTranslation(translation, params);
}

/**
 * Formatteer Nederlandse vertaling met parameters
 * @param {string} text - Tekst met plaatshouders
 * @param {object} params - Parameters die plaatshouders vervangen
 * @returns {string} Geformatteerde tekst
 */
function formatDutchMathPuzzleTranslation(text, params) {
  let formattedText = text;
  for (const [key, value] of Object.entries(params)) {
    formattedText = formattedText.replace(new RegExp(`\\{${key}\\}`, 'g'), value);
  }
  return formattedText;
}

/**
 * Valideer Nederlandse vertalingen
 * @returns {object} Validatieresultaat
 */
function validateDutchMathPuzzleTranslations() {
  const keys = Object.keys(MATH_PUZZLE_TRANSLATIONS_NL.nl);
  const missingKeys = [];
  const emptyTranslations = [];

  // Controleer lege vertalingen
  keys.forEach(key => {
    const value = MATH_PUZZLE_TRANSLATIONS_NL.nl[key];
    if (!value || value.trim() === '') {
      emptyTranslations.push(key);
    }
  });

  return {
    totalKeys: keys.length,
    validKeys: keys.length - emptyTranslations.length,
    missingKeys,
    emptyTranslations,
    coverage: `${((keys.length - emptyTranslations.length) / keys.length * 100).toFixed(1)}%`,
    isComplete: emptyTranslations.length === 0
  };
}

