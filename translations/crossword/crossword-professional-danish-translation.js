/**
 * Helper function to get Danish translation for a key
 * @param {string} key - Translation key
 * @returns {string} Danish translation
 */
function getDanishCrosswordTranslation(key) {
  return CROSSWORD_TRANSLATIONS_DA.da[key] || key;
}

/**
 * Format Danish translation with placeholders
 * @param {string} text - Text with placeholders
 * @param {...any} values - Values to replace placeholders
 * @returns {string} Formatted text
 */
function formatDanishCrosswordTranslation(text, ...values) {
  let formatted = text;

  // Handle object with named parameters
  if (values.length === 1 && typeof values[0] === 'object' && values[0] !== null) {
    const params = values[0];
    for (const [key, value] of Object.entries(params)) {
      formatted = formatted.replace(new RegExp(`\\{${key}\\}`, 'g'), value);
    }
  }
  // Handle positional parameters
  else {
    values.forEach((value, index) => {
      formatted = formatted.replace(new RegExp(`\\{${index}\\}`, 'g'), value);
    });
  }

  return formatted;
}

/**
 * Validate Danish translations
 * @returns {object} Validation results
 */
function validateDanishCrosswordTranslations() {
  const expectedKeys = 191;
  const actualKeys = Object.keys(CROSSWORD_TRANSLATIONS_DA.da).length;
  const criticalKeys = [
    "crossword.term.across",
    "crossword.term.down",
    "crossword.term.clue",
    "crossword.generate.worksheet",
    "crossword.generate.answer"
  ];

  const missingCritical = criticalKeys.filter(key => !CROSSWORD_TRANSLATIONS_DA.da[key]);

  return {
    language: "Danish",
    code: "da",
    totalKeys: actualKeys,
    expectedKeys: expectedKeys,
    isComplete: actualKeys === expectedKeys,
    criticalKeysPresent: missingCritical.length === 0,
    missingCritical: missingCritical,
    coverage: ((actualKeys / expectedKeys) * 100).toFixed(1) + '%'
  };
}

