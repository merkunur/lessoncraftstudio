// Helper function to get translated theme name
export function getTranslatedThemeName(themeName: string, locale: string): string {
  const cleanName = themeName.toLowerCase().replace(/[- _]/g, '');
  
  // Check exact match first
  if (themeTranslations[cleanName]) {
    return themeTranslations[cleanName][locale] || 
           themeTranslations[cleanName]['en'] || 
           formatDefaultName(themeName);
  }
  
  // Check for nested themes (e.g., "alphabetsvg/cursive")
  const parts = themeName.split('/');
  if (parts.length > 1) {
    const translatedParts = parts.map(part => {
      const cleanPart = part.toLowerCase().replace(/[- _]/g, '');
      if (themeTranslations[cleanPart]) {
        return themeTranslations[cleanPart][locale] || 
               themeTranslations[cleanPart]['en'] || 
               formatDefaultName(part);
      }
      return formatDefaultName(part);
    });
    return translatedParts.join(' / ');
  }
  
  return formatDefaultName(themeName);
}

// Helper function to get translated image name
export function getTranslatedImageName(imageName: string, locale: string): string {
  const baseName = imageName.toLowerCase()
    .replace(/\.(png|jpg|jpeg|gif|svg)$/i, '')
    .replace(/[- _]/g, '');
  
  if (imageTranslations[baseName]) {
    return imageTranslations[baseName][locale] || 
           imageTranslations[baseName]['en'] || 
           formatDefaultName(imageName);
  }
  
  // Try to find partial matches
  for (const [key, translations] of Object.entries(imageTranslations)) {
    if (baseName.includes(key) || key.includes(baseName)) {
      return translations[locale] || translations['en'] || formatDefaultName(imageName);
    }
  }
  
  return formatDefaultName(imageName);
}

// Format name for display
function formatDefaultName(name: string): string {
  return name
    .replace(/\.(png|jpg|jpeg|gif|svg)$/i, '')
    .replace(/[-_]/g, ' ')
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}