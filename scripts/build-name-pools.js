/**
 * Build name-pools.js — locale-appropriate first names for templates
 * ==================================================================
 * Validates the source JSON and compiles to a browser-loadable JS file.
 *
 * Run: node scripts/build-name-pools.js
 *
 * Reads:  scripts/v2-data/name-pools.json
 * Writes: REFERENCE TRANSLATIONS/name-pools.js
 */

const fs = require('fs');
const path = require('path');

const SRC_PATH = path.join(__dirname, 'v2-data', 'name-pools.json');
const OUT_PATH = path.join(__dirname, '..', 'REFERENCE TRANSLATIONS', 'name-pools.js');

const LOCALES = ['en', 'de', 'fr', 'es', 'pt', 'it', 'nl', 'sv', 'da', 'no', 'fi'];
const GENDERS = ['male', 'female'];
const EXPECTED_COUNT = 15;
const MAX_NAME_LENGTH = 8;

function main() {
  console.log('Building name-pools.js...\n');

  const src = JSON.parse(fs.readFileSync(SRC_PATH, 'utf8'));
  let errors = 0;

  // ============================================================
  // VALIDATION
  // ============================================================

  // Check all locales present
  for (const locale of LOCALES) {
    if (!src[locale]) {
      console.error('ERROR: Missing locale:', locale);
      errors++;
      continue;
    }

    for (const gender of GENDERS) {
      if (!src[locale][gender]) {
        console.error('ERROR: Missing', locale + '.' + gender);
        errors++;
        continue;
      }

      const names = src[locale][gender];

      // Check count
      if (names.length !== EXPECTED_COUNT) {
        console.error('ERROR:', locale + '.' + gender, 'has', names.length, 'names, expected', EXPECTED_COUNT);
        errors++;
      }

      // Check for duplicates within the list
      const seen = new Set();
      for (const name of names) {
        const lower = name.toLowerCase();
        if (seen.has(lower)) {
          console.error('ERROR: Duplicate name in', locale + '.' + gender + ':', name);
          errors++;
        }
        seen.add(lower);
      }

      // Check name lengths
      for (const name of names) {
        if (name.length > MAX_NAME_LENGTH) {
          console.error('WARNING: Long name in', locale + '.' + gender + ':', name, '(' + name.length + ' chars)');
        }
        if (name.length < 2) {
          console.error('ERROR: Too short name in', locale + '.' + gender + ':', name);
          errors++;
        }
      }

      // Check proper capitalization
      for (const name of names) {
        if (name[0] !== name[0].toUpperCase()) {
          console.error('ERROR: Not capitalized in', locale + '.' + gender + ':', name);
          errors++;
        }
      }
    }

    // Check no overlap between male and female names within a locale
    if (src[locale].male && src[locale].female) {
      const maleSet = new Set(src[locale].male.map(n => n.toLowerCase()));
      for (const name of src[locale].female) {
        if (maleSet.has(name.toLowerCase())) {
          console.error('WARNING: Name appears in both male and female for', locale + ':', name);
        }
      }
    }
  }

  if (errors > 0) {
    console.error('\n' + errors + ' error(s) found. Fix before building.');
    process.exit(1);
  }

  // ============================================================
  // BUILD OUTPUT
  // ============================================================

  // Build clean object (strip __comment)
  const pools = {};
  for (const locale of LOCALES) {
    pools[locale] = {
      male: src[locale].male,
      female: src[locale].female,
    };
  }

  const totalNames = LOCALES.length * GENDERS.length * EXPECTED_COUNT;

  const jsContent = `/**
 * Name Pools — locale-appropriate first names for templates
 * ==========================================================
 * Auto-generated by scripts/build-name-pools.js
 * DO NOT EDIT MANUALLY
 *
 * ${EXPECTED_COUNT} names per gender per locale, ${LOCALES.length} locales = ${totalNames} total names.
 *
 * Generated: ${new Date().toISOString().split('T')[0]}
 */

/* eslint-disable */
// @ts-nocheck

const NAME_POOLS = ${JSON.stringify(pools, null, 2)};

/**
 * NameHelper — pick names from pools
 *
 * Usage:
 *   NameHelper.pick('en', 'male', rng)            → "Tom"
 *   NameHelper.pick('de', 'female', rng)           → "Emma"
 *   NameHelper.pickUnique('en', 'male', 3, rng)    → ["Ben","Sam","Max"]
 *   NameHelper.pickPair('en', rng)                 → { name1: "Tom", name2: "Emma", g1: "male", g2: "female" }
 *   NameHelper.all('en', 'male')                   → ["Tom","Jack","Ben",...]
 */
const NameHelper = {
  /**
   * Pick a random name from the pool.
   * @param {string} locale - Locale code
   * @param {string} gender - "male" or "female"
   * @param {{ next: function }} rng - RNG with .next() returning [0,1)
   * @returns {string} A name
   */
  pick(locale, gender, rng) {
    var pool = (NAME_POOLS[locale] || NAME_POOLS.en)[gender];
    return pool[Math.floor(rng.next() * pool.length)];
  },

  /**
   * Pick N unique names from the pool.
   * @param {string} locale - Locale code
   * @param {string} gender - "male" or "female" or "mixed"
   * @param {number} count - How many names
   * @param {{ next: function }} rng - RNG with .next() returning [0,1)
   * @returns {string[]} Array of unique names
   */
  pickUnique(locale, gender, count, rng) {
    var localeData = NAME_POOLS[locale] || NAME_POOLS.en;
    var pool;
    if (gender === 'mixed') {
      pool = localeData.male.concat(localeData.female);
    } else {
      pool = localeData[gender];
    }

    // Fisher-Yates shuffle on a copy
    var shuffled = pool.slice();
    for (var i = shuffled.length - 1; i > 0; i--) {
      var j = Math.floor(rng.next() * (i + 1));
      var tmp = shuffled[i]; shuffled[i] = shuffled[j]; shuffled[j] = tmp;
    }

    return shuffled.slice(0, Math.min(count, shuffled.length));
  },

  /**
   * Pick a pair of names (one male, one female by default).
   * @param {string} locale - Locale code
   * @param {{ next: function }} rng - RNG with .next() returning [0,1)
   * @returns {{ name1: string, name2: string, g1: string, g2: string }}
   */
  pickPair(locale, rng) {
    return {
      name1: this.pick(locale, 'male', rng),
      name2: this.pick(locale, 'female', rng),
      g1: 'male',
      g2: 'female'
    };
  },

  /**
   * Get all names for a locale and gender.
   * @param {string} locale - Locale code
   * @param {string} gender - "male" or "female"
   * @returns {string[]}
   */
  all(locale, gender) {
    return (NAME_POOLS[locale] || NAME_POOLS.en)[gender].slice();
  }
};
`;

  fs.writeFileSync(OUT_PATH, jsContent, 'utf8');

  // ============================================================
  // STATS
  // ============================================================

  const fileSize = fs.statSync(OUT_PATH).size;
  console.log('=== Build Complete ===');
  console.log('Output:', OUT_PATH);
  console.log('File size:', (fileSize / 1024).toFixed(1) + ' KB');
  console.log('Total names:', totalNames);
  console.log('Locales:', LOCALES.length);
  console.log('Names per gender:', EXPECTED_COUNT);

  // Print summary table
  console.log('\nNames by locale:');
  for (const locale of LOCALES) {
    const m = pools[locale].male.join(', ');
    const f = pools[locale].female.join(', ');
    console.log('  ' + locale + ' M: ' + m);
    console.log('  ' + locale + ' F: ' + f);
  }
}

main();
