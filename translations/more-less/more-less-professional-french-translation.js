// Helper function to get a translation
function getMoreLessTranslation(key, locale = 'fr', params = {}) {
  const translations = MORE_LESS_FRENCH_TRANSLATIONS[locale] || MORE_LESS_FRENCH_TRANSLATIONS['fr'];
  let text = translations[key] || key;

  // Replace parameters like {count}, {num}, etc.
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Helper function to get all translations for a locale
function getAllMoreLessTranslations(locale = 'fr') {
  return MORE_LESS_FRENCH_TRANSLATIONS[locale] || MORE_LESS_FRENCH_TRANSLATIONS['fr'];
}

