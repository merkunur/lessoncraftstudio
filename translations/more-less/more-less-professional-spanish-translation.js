// Helper function to get a translation
function getMoreLessTranslation(key, locale = 'es', params = {}) {
  const translations = MORE_LESS_SPANISH_TRANSLATIONS[locale] || MORE_LESS_SPANISH_TRANSLATIONS['es'];
  let text = translations[key] || key;

  // Replace parameters like {count}, {num}, etc.
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Helper function to get all translations for a locale
function getAllMoreLessTranslations(locale = 'es') {
  return MORE_LESS_SPANISH_TRANSLATIONS[locale] || MORE_LESS_SPANISH_TRANSLATIONS['es'];
}

