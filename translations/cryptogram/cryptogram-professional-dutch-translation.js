/**
 * Get Dutch translation for a key
 * @param {string} key - Translation key
 * @param {object} params - Parameters for interpolation
 * @returns {string} Translated text in Dutch
 */
function getDutchCryptogramTranslation(key, params = {}) {
  const translation = CRYPTOGRAM_TRANSLATIONS_NL.nl[key] ||
                     CRYPTOGRAM_TRANSLATIONS_NL.en?.[key] ||
                     key;

  return formatDutchCryptogramTranslation(translation, params);
}

/**
 * Format Dutch translation with parameters
 * @param {string} text - Text with placeholders
 * @param {object} params - Parameters to replace
 * @returns {string} Formatted text
 */
function formatDutchCryptogramTranslation(text, params) {
  let formattedText = text;
  for (const [key, value] of Object.entries(params)) {
    formattedText = formattedText.replace(new RegExp(`\\{${key}\\}`, 'g'), value);
  }
  return formattedText;
}

/**
 * Validate Dutch translations
 * @returns {object} Validation result
 */
function validateDutchCryptogramTranslations() {
  const keys = Object.keys(CRYPTOGRAM_TRANSLATIONS_NL.nl);
  const totalExpected = 194;

  return {
    valid: keys.length === totalExpected,
    coverage: `${((keys.length / totalExpected) * 100).toFixed(1)}%`,
    missing: totalExpected - keys.length,
    total: keys.length,
    expected: totalExpected
  };
}

