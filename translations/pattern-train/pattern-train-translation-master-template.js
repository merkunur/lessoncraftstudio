// Helper function to get translation with fallback
function getTranslation(key, locale = 'en', params = {}) {
  const translation = PATTERN_TRAIN_TRANSLATIONS[locale]?.[key] ||
                     PATTERN_TRAIN_TRANSLATIONS['en'][key] ||
                     key;

  // Handle parameter substitution
  return formatTranslation(translation, params);
}

// Format translation with parameters
function formatTranslation(template, params) {
  return template.replace(/\{(\w+)\}/g, (match, key) => {
    return params.hasOwnProperty(key) ? params[key] : match;
  });
}

// Check translation coverage
function getTranslationCoverage(locale) {
  const totalKeys = Object.keys(PATTERN_TRAIN_TRANSLATIONS.en).length;
  const translatedKeys = Object.keys(PATTERN_TRAIN_TRANSLATIONS[locale] || {}).length;
  return {
    total: totalKeys,
    translated: translatedKeys,
    percentage: Math.round((translatedKeys / totalKeys) * 100)
  };
}

