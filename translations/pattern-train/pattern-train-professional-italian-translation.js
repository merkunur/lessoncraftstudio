// Helper function to get translation with fallback
function getPatternTrainTranslation(key, locale = 'it', params = {}) {
  const translations = PATTERN_TRAIN_ITALIAN_TRANSLATIONS[locale] || PATTERN_TRAIN_ITALIAN_TRANSLATIONS['it'];
  let text = translations[key] || key;

  // Handle parameter substitution
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Helper function to get all translations for a locale
function getAllPatternTrainTranslations(locale = 'it') {
  return PATTERN_TRAIN_ITALIAN_TRANSLATIONS[locale] || PATTERN_TRAIN_ITALIAN_TRANSLATIONS['it'];
}

