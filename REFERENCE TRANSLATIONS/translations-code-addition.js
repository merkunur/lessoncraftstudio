// Helper function for getting translations
window.t = function(key, params = {}) {
    const locale = window.currentLocale || 'en';

    // Get translation from CODE_ADDITION_TRANSLATIONS if available
    if (typeof CODE_ADDITION_TRANSLATIONS !== 'undefined') {
        const translation = CODE_ADDITION_TRANSLATIONS[locale]?.[key] ||
                           CODE_ADDITION_TRANSLATIONS['en']?.[key] ||
                           key;

        // If params provided, use formatTranslation
        if (params && Object.keys(params).length > 0) {
            return window.formatTranslation(translation, params);
        }
        return translation;
    }
    return key;
};

// Format translation with parameters
window.formatTranslation = function(text, params = {}) {
    let result = text;
    if (params && typeof params === 'object') {
        Object.keys(params).forEach(key => {
            result = result.replace(new RegExp(`\{${key}\}`, 'g'), params[key]);
        });
    }
    return result;
};

// Also make available without window prefix
if (typeof window !== 'undefined') {
    var t = window.t;
    var formatTranslation = window.formatTranslation;
}

