// Helper function to get a translation
function getMoreLessTranslation(key, locale = 'no', params = {}) {
  const translations = MORE_LESS_NORWEGIAN_TRANSLATIONS[locale] || MORE_LESS_NORWEGIAN_TRANSLATIONS['no'];
  let text = translations[key] || key;

  // Replace parameters like {count}, {num}, etc.
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Helper function to get all translations for a locale
function getAllMoreLessTranslations(locale = 'no') {
  return MORE_LESS_NORWEGIAN_TRANSLATIONS[locale] || MORE_LESS_NORWEGIAN_TRANSLATIONS['no'];
}

