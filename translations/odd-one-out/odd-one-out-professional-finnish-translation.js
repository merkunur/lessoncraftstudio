// Helper function to get translation with fallback
function getOddOneOutTranslation(key, locale = 'fi', params = {}) {
  const translations = ODD_ONE_OUT_FINNISH_TRANSLATIONS[locale] || ODD_ONE_OUT_FINNISH_TRANSLATIONS['fi'];
  let text = translations[key] || key;

  // Handle parameter substitution
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Helper function to get all translations for a locale
function getAllOddOneOutTranslations(locale = 'fi') {
  return ODD_ONE_OUT_FINNISH_TRANSLATIONS[locale] || ODD_ONE_OUT_FINNISH_TRANSLATIONS['fi'];
}

