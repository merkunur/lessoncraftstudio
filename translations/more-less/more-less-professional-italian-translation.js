// Helper function to get a translation
function getMoreLessTranslation(key, locale = 'it', params = {}) {
  const translations = MORE_LESS_ITALIAN_TRANSLATIONS[locale] || MORE_LESS_ITALIAN_TRANSLATIONS['it'];
  let text = translations[key] || key;

  // Replace parameters like {count}, {num}, etc.
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Helper function to get all translations for a locale
function getAllMoreLessTranslations(locale = 'it') {
  return MORE_LESS_ITALIAN_TRANSLATIONS[locale] || MORE_LESS_ITALIAN_TRANSLATIONS['it'];
}

