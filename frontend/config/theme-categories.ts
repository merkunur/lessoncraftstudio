/** Find a category by its ID */
export function getCategoryById(id: string): ThemeCategory | undefined {
  return THEME_CATEGORIES.find(c => c.id === id);
}

/** Find which category a theme belongs to */
export function getCategoryForTheme(themeId: string): ThemeCategory | undefined {
  return THEME_CATEGORIES.find(c => (c.themes as string[]).includes(themeId));
}
