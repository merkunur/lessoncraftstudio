// Helper function to get translation with fallback
function getOddOneOutTranslation(key, locale = 'sv', params = {}) {
  const translations = ODD_ONE_OUT_SWEDISH_TRANSLATIONS[locale] || ODD_ONE_OUT_SWEDISH_TRANSLATIONS['sv'];
  let text = translations[key] || key;

  // Handle parameter substitution
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Helper function to get all translations for a locale
function getAllOddOneOutTranslations(locale = 'sv') {
  return ODD_ONE_OUT_SWEDISH_TRANSLATIONS[locale] || ODD_ONE_OUT_SWEDISH_TRANSLATIONS['sv'];
}

