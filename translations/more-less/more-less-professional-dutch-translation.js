// Helper function to get a translation
function getMoreLessTranslation(key, locale = 'nl', params = {}) {
  const translations = MORE_LESS_DUTCH_TRANSLATIONS[locale] || MORE_LESS_DUTCH_TRANSLATIONS['nl'];
  let text = translations[key] || key;

  // Replace parameters like {count}, {num}, etc.
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Helper function to get all translations for a locale
function getAllMoreLessTranslations(locale = 'nl') {
  return MORE_LESS_DUTCH_TRANSLATIONS[locale] || MORE_LESS_DUTCH_TRANSLATIONS['nl'];
}

